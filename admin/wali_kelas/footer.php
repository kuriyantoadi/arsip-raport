<script>

function raport_asli() {
  var x = document.getElementById("raport-asli");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}

function raport_scan() {
  var x = document.getElementById("raport-scan");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}
</script>
